#include <stdio.h>
#include <stdlib.h>
#include <string.h>
/* Reverse alphabet */
/**
 * main - Entry point
 *
 * Return: ALways 0 (Success)
 */
int main(void)
{
	char base16[] = "abcdefghijklmnopqrstuvwxyz";
	int i;

	for (i = 25; i = 0; i--)
	{
		putchar(base16[i]);
	}

	putchar('\n');

	return (0);
}
